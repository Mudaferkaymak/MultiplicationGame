    private static Date parseDate(String dateStr) throws ParseException {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
        return dateFormat.parse(dateStr);
    }
 public int getID(int selected){
     //Ebeveynin seçtiği oyunun bilgilerini ayrıntıların gösterildiği ekrana göndermek üzere ID'si bulunur
     int counter =0;
     String fileName = "fullGames.csv";

     //Dosya okunarak seçilen satırdaki oyunun ID'si return edilir
    try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] values = line.split(COMMA_DELIMITER);

            // Record verilerini oku
            if(selected==counter){
              return Integer.parseInt(values[8]);   
            }
           counter++;
            // Yeni Record nesnesini oluştur ve tabloya ekle
 
         
        }
    } catch (IOException | NumberFormatException e) {
        e.printStackTrace();
    }
     return 0;
 }

