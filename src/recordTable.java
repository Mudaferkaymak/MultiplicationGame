        String fileName = "fullGames.csv";
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0); // Tabloyu temizle
        
        //fullGames dosyasından değerler tek tek okunur ve tabloya yazdırılmak üzere tableModele aktarılır
        try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] values = line.split(COMMA_DELIMITER);

                // Record verilerini oku
                int exercise = Integer.parseInt(values[0]);
                String name = values[1];
                String date = values[2];
                int totalCorrect = Integer.parseInt(values[3]);
                float correctScore = Float.parseFloat(values[4]);
                float timeScore = Float.parseFloat(values[5]);
                float totalScore = Float.parseFloat(values[6]);
                int totalTime = Integer.parseInt(values[7]);

                // Yeni Record nesnesini oluştur ve tabloya ekle
                Object[] row = { exercise, name, date, totalCorrect, correctScore, timeScore, totalScore, totalTime };
                tableModel.addRow(row);
            }
        } catch (IOException | NumberFormatException e) {
            e.printStackTrace();
        }
